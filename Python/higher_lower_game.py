from random import randint
from os import system, name


def main():
    print(logo)
    score = 0

    # Randomly select data from data_dict
    random_data_B = data[random_celeb()]

    # Main game loop
    while True:
        # a = b, so that in next loop, b becomes a and b get new data
        random_data_A = random_data_B
        random_data_B = data[random_celeb()]

        print(f"Compare A: {random_data_A['name']}, {random_data_A['description']}, from {random_data_A['country']}")
        print(vs)
        print(f"Compare B: {random_data_B['name']}, {random_data_B['description']}, from {random_data_B['country']}")

        choice = input("Who has more followers? Type 'A' or 'B': ").lower()
        clear()

        # If user is right, continue game with score += 1
        if check_followers(choice, random_data_A['follower_count'], random_data_B['follower_count']):
            score += 1
            print(f"\nYou're right! Your score: {score}")
        else:
            print("Wrong choice.. Game over!" if choice not in ('a', 'b') else f"\nYou Loose! Your score {score}")
            break


def random_celeb():
    """Randomly gives an index for data-list if it was not already parsed"""
    celeb_index_memory = []
    while True:
        celeb_index = randint(0, len(data) - 1)
        if celeb_index not in celeb_index_memory:
            celeb_index_memory.append(celeb_index)
            return celeb_index


def check_followers(choice, a_followers, b_followers):
    """Return True or False, if users choice is correct or not"""
    if a_followers > b_followers:
        return choice == 'a'
    else:
        return choice == 'b'


