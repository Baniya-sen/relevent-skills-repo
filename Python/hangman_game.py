"""A simple Hangman game in python"""
import random


def main():
    greet()

    choice = random.choice(word_list)
    word_len = len(choice)
    display = []
    lives = 6

    for i in range(word_len):
        display.append("_")

    while True:
        guess = input("Enter a letter: ").lower()

        if len(guess) != 1:
            print("Enter only 1 letter!")
            continue

        if guess in display:
            print(f"You already entered this letter '{guess}'!")
            continue

        for position in range(word_len):
            if guess == choice[position]:
                display[position] = guess

        if guess not in choice:
            print(f"Your guess {guess} is wrong! You lose a life!")
            lives -= 1

        print(' '.join(display))
        print(stages[lives])

        if "_" not in display:
            print("Game Over! You Win!")
            break

        if lives == 0:
            print("Game Over! You Lose!")
            break


